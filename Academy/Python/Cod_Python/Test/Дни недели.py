while True:
    print("\nДни недели")
    x = int(input("Введите числовое значение: "))    
    if x == 1:
        print(f"\nПонедельник")
    elif x == 2:
        print(f"\nВторник")
    elif x == 3:
        print(f"\nСреда")
    elif x == 4:
        print(f"\nЧетверг")
    elif x == 5:
        print(f"\nПчтница")
    elif x == 6:
        print(f"\nСуббота")
    elif x == 7:
        print(f"\nВоскресенье")
    else:
        print(f"\nВвод не верен. Попробуйте снова.")
    
   
